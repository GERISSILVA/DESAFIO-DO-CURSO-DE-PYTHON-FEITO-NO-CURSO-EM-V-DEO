r1 = int(input('Escreva o cumprimento da reta 1: '))
r2 = int(input('Escreva o cumprimento da reta 2: '))
r3 = int(input('Escreva o cumprimento da reta 3: '))

if r1 + r2 > r3:
    print('As retas podem fazer um triângulo ')
else:
    print('Não e possivel formar um triângulo com essa retas !')
